public class THPStatus {
	
	private long userId;
	private long userOnline;
	private String userLoginIP;
	private String userLoginTime;
	private String userUpdateTime;
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public long getUserOnline() {
		return userOnline;
	}
	public void setUserOnline(long userOnline) {
		this.userOnline = userOnline;
	}
	public String getUserLoginIP() {
		return userLoginIP;
	}
	public void setUserLoginIP(String userLoginIP) {
		this.userLoginIP = userLoginIP;
	}
	public String getUserLoginTime() {
		return userLoginTime;
	}
	public void setUserLoginTime(String userLoginTime) {
		this.userLoginTime = userLoginTime;
	}
	public String getUserUpdateTime() {
		return userUpdateTime;
	}
	public void setUserUpdateTime(String userUpdateTime) {
		this.userUpdateTime = userUpdateTime;
	}
	@Override
	public String toString() {
		return "THPStatus [userId=" + userId + ", userOnline=" + userOnline + ", userLoginIP=" + userLoginIP
				+ ", userLoginTime=" + userLoginTime + ", userUpdateTime=" + userUpdateTime + "]";
	}
		

}
